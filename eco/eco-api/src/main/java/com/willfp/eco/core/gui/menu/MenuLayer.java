package com.willfp.eco.core.gui.menu;

/**
 * Different layers of the menu.
 */
public enum MenuLayer {
    /**
     * Right at the back.
     */
    BACKGROUND,

    /**
     * Second from the back.
     */
    LOWER,

    /**
     * In the middle (default).
     */
    MIDDLE,

    /**
     * Near the top.
     */
    UPPER,

    /**
     * At the absolute top.
     */
    TOP
}
